# Using slice notation to extract a substring from a string
text = "Hello, World!"

# Extracting "Hello"
substring = text[:5]
print(substring)   # Output: Hello

# Using slice notation to reverse a list
numbers = [1, 2, 3, 4, 5]
reversed_list = numbers[::-1]
print(reversed_list)   # Output: [5, 4, 3, 2, 1]
